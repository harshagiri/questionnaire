#!/usr/bin/env node

import { PrismaClient } from "@prisma/client";
import { PrismaPg } from "@prisma/adapter-pg";
import { existsSync } from "node:fs";
import { readFileSync } from "node:fs";

const PROM_AUDIT_KEYS = {
  payload: "__promAutoAudit",
  instrument: "__promAutoInstrument",
  percent: "__promAutoPercent",
  severity: "__promAutoSeverity",
  generatedAt: "__promAutoGeneratedAt",
};

const neckPrimaryReasons = new Set([
  "neck-pain",
  "arm-pain",
  "numbness",
  "weakness",
  "walking-difficulty",
]);

const odiItems = [
  "odiPainIntensity",
  "odiPersonalCare",
  "odiLifting",
  "odiWalking",
  "odiSitting",
  "odiStanding",
  "odiSleeping",
  "odiSexLife",
  "odiSocialLife",
  "odiTravelling",
];

const ndiItems = [
  "ndiPainIntensity",
  "ndiPersonalCare",
  "ndiLifting",
  "ndiReading",
  "ndiHeadaches",
  "ndiConcentration",
  "ndiWork",
  "ndiDriving",
  "ndiSleeping",
  "ndiRecreation",
];

function normalizeConnectionString(raw) {
  try {
    const url = new URL(raw);
    const runningInDocker = existsSync("/.dockerenv");

    if (url.hostname === "postgres") {
      url.hostname = process.env.DATABASE_HOST_OVERRIDE || (runningInDocker ? "postgres" : "localhost");
    }

    return url.toString();
  } catch {
    return raw;
  }
}

function loadDotEnv() {
  if (process.env.DATABASE_URL) {
    return;
  }

  if (!existsSync(".env")) {
    return;
  }

  const file = readFileSync(".env", "utf8");
  for (const line of file.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) {
      continue;
    }

    const index = trimmed.indexOf("=");
    const key = trimmed.slice(0, index).trim();
    if (key !== "DATABASE_URL") {
      continue;
    }

    let value = trimmed.slice(index + 1).trim();
    if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1);
    }

    if (value) {
      process.env.DATABASE_URL = value;
    }
    return;
  }
}

function createPrisma() {
  loadDotEnv();
  const configured = process.env.DATABASE_URL;
  if (!configured) {
    throw new Error("DATABASE_URL is required");
  }

  const adapter = new PrismaPg({ connectionString: normalizeConnectionString(configured) });
  return new PrismaClient({ adapter });
}

function toFiniteNumber(value) {
  if (typeof value === "number" && Number.isFinite(value)) {
    return value;
  }

  if (typeof value === "string" && value.trim().length > 0) {
    const parsed = Number(value);
    return Number.isFinite(parsed) ? parsed : null;
  }

  return null;
}

function normalizePromItemScore(value) {
  const parsed = toFiniteNumber(value);
  if (parsed === null || parsed < 0 || parsed > 5) {
    return null;
  }

  return Math.round(parsed);
}

function roundOne(value) {
  return Math.round(value * 10) / 10;
}

function odiSeverityFromPercent(percent) {
  if (percent <= 20) return "Minimal";
  if (percent <= 40) return "Moderate";
  if (percent <= 60) return "Severe";
  if (percent <= 80) return "Crippling";
  return "Bed-bound / exaggeration";
}

function ndiSeverityFromRaw(rawScore) {
  if (rawScore <= 4) return "No disability";
  if (rawScore <= 14) return "Minimal";
  if (rawScore <= 24) return "Moderate";
  if (rawScore <= 34) return "Severe";
  return "Complete disability";
}

function inferInstrument(answers) {
  return neckPrimaryReasons.has(answers.q1PrimaryReason) ? "NDI" : "ODI";
}

function computePromScore(answers) {
  const instrument = inferInstrument(answers);
  const keys = instrument === "ODI" ? odiItems : ndiItems;

  const itemScores = keys.map((key) => normalizePromItemScore(answers[key]));
  const answeredItems = itemScores.filter((score) => score !== null).length;
  const totalScore = itemScores.reduce((sum, score) => sum + (score ?? 0), 0);
  const maxScore = answeredItems * 5;
  const expectedItems = keys.length;
  const isComplete = answeredItems === expectedItems;
  const percent = maxScore > 0 ? roundOne((totalScore / maxScore) * 100) : null;

  let severity = "Not enough data";
  if (percent !== null) {
    if (instrument === "ODI") {
      severity = odiSeverityFromPercent(percent);
    } else if (isComplete) {
      severity = ndiSeverityFromRaw(totalScore);
    } else {
      severity = odiSeverityFromPercent(percent);
    }
  }

  return {
    instrument,
    totalScore,
    answeredItems,
    expectedItems,
    maxScore,
    percent,
    severity,
    isComplete,
    itemScores: keys.map((key, index) => ({ key, score: itemScores[index] })),
  };
}

function buildAuditPayload(answers) {
  const score = computePromScore(answers);
  return {
    version: "prom-v1-strict",
    generatedAt: new Date().toISOString(),
    instrument: score.instrument,
    totalScore: score.totalScore,
    answeredItems: score.answeredItems,
    expectedItems: score.expectedItems,
    maxScore: score.maxScore,
    percent: score.percent,
    severity: score.severity,
    isComplete: score.isComplete,
    itemScores: score.itemScores,
  };
}

function getArgs() {
  const apply = process.argv.includes("--apply");
  const force = process.argv.includes("--force");
  const sampleArg = process.argv.find((arg) => arg.startsWith("--sample="));
  const sample = sampleArg ? Number(sampleArg.split("=")[1]) : 5;

  return {
    apply,
    force,
    sample: Number.isFinite(sample) && sample >= 0 ? Math.floor(sample) : 5,
  };
}

async function main() {
  const { apply, force, sample } = getArgs();
  const prisma = createPrisma();

  try {
    const submissions = await prisma.questionnaireSubmission.findMany({
      where: {
        sessionId: {
          not: {
            endsWith: ":doctor",
          },
        },
      },
      select: {
        id: true,
        sessionId: true,
        answers: {
          select: {
            key: true,
            value: true,
          },
        },
      },
      orderBy: {
        updatedAt: "desc",
      },
    });

    let skippedExistingAudit = 0;
    let skippedNoPromData = 0;
    let skippedNoPercent = 0;
    const candidates = [];

    for (const submission of submissions) {
      const hasExistingAudit = submission.answers.some((row) => row.key === PROM_AUDIT_KEYS.payload);
      if (hasExistingAudit && !force) {
        skippedExistingAudit += 1;
        continue;
      }

      const rawAnswers = Object.fromEntries(
        submission.answers
          .filter((row) => !row.key.startsWith("__"))
          .map((row) => [row.key, row.value]),
      );

      const hasPromInputs =
        Object.prototype.hasOwnProperty.call(rawAnswers, "q1PrimaryReason") ||
        odiItems.some((key) => Object.prototype.hasOwnProperty.call(rawAnswers, key)) ||
        ndiItems.some((key) => Object.prototype.hasOwnProperty.call(rawAnswers, key));

      if (!hasPromInputs) {
        skippedNoPromData += 1;
        continue;
      }

      const audit = buildAuditPayload(rawAnswers);
      if (audit.percent === null) {
        skippedNoPercent += 1;
        continue;
      }

      candidates.push({
        submissionId: submission.id,
        sessionId: submission.sessionId,
        audit,
      });
    }

    const summary = {
      mode: apply ? "apply" : "dry-run",
      force,
      totalSubmissions: submissions.length,
      candidateUpdates: candidates.length,
      skippedExistingAudit,
      skippedNoPromData,
      skippedNoPercent,
    };

    console.log(JSON.stringify(summary, null, 2));

    if (sample > 0 && candidates.length > 0) {
      const sampleRows = candidates.slice(0, sample).map((row) => ({
        sessionId: row.sessionId,
        instrument: row.audit.instrument,
        percent: row.audit.percent,
        severity: row.audit.severity,
      }));
      console.log("Sample candidates:");
      console.table(sampleRows);
    }

    if (!apply) {
      return;
    }

    for (const row of candidates) {
      await prisma.$transaction(async (tx) => {
        await tx.questionnaireAnswer.deleteMany({
          where: {
            submissionId: row.submissionId,
            key: {
              in: Object.values(PROM_AUDIT_KEYS),
            },
          },
        });

        await tx.questionnaireAnswer.createMany({
          data: [
            {
              submissionId: row.submissionId,
              key: PROM_AUDIT_KEYS.payload,
              value: row.audit,
            },
            {
              submissionId: row.submissionId,
              key: PROM_AUDIT_KEYS.instrument,
              value: row.audit.instrument,
            },
            {
              submissionId: row.submissionId,
              key: PROM_AUDIT_KEYS.percent,
              value: row.audit.percent ?? "",
            },
            {
              submissionId: row.submissionId,
              key: PROM_AUDIT_KEYS.severity,
              value: row.audit.severity,
            },
            {
              submissionId: row.submissionId,
              key: PROM_AUDIT_KEYS.generatedAt,
              value: row.audit.generatedAt,
            },
          ],
        });
      });
    }

    console.log(`Applied PROM backfill to ${candidates.length} submissions.`);
  } finally {
    await prisma.$disconnect();
  }
}

main().catch((error) => {
  console.error("PROM backfill failed:", error);
  process.exit(1);
});
